/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	setTarget(&motor1, 1);
	setTarget(&motor2, 1);
	setTarget(&motor3, 1);

	setStep(&motor1, step);
	setStep(&motor2, step);
	setStep(&motor3, step);

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */
  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */
  HAL_Delay(500);
  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM3_Init();
  MX_SPI1_Init();
  MX_I2C1_Init();
  MX_TIM2_Init();
  MX_TIM1_Init();
  MX_TIM5_Init();
  MX_TIM9_Init();
  MX_TIM4_Init();
  /* USER CODE BEGIN 2 */

	Commutation_Start(&motor1, rps1 * 7.0f * 6.0f);
	Commutation_Start(&motor2, rps2 * 7.0f * 6.0f);
	Commutation_Start(&motor3, rps3 * 7.0f * 6.0f);

	uint8_t duty = 98;
	SetDuty_TIM3_CH2(&motor1, duty);

	//HAL_SPI_Receive_IT(&hspi1, spi_rx_buf, SPI_FRAME_LEN); // wait for first 10 bytes
	init_imu(&imu);

	HAL_TIM_Base_Start_IT(&htim1);

	for (int i = 0; i < SPI_FRAME_LEN; i++) {
		spi_rx_buf[i] = 0;
	}
	for (int i = 0; i < SPI_FRAME_LEN; i++) {
		spi_tx_buf[i] = i;
	}
	HAL_SPI_TransmitReceive_IT(&hspi1, spi_tx_buf, spi_rx_buf, SPI_FRAME_LEN);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
	int time_previous = HAL_GetTick();

	int t1 = 5000;
	int target1 = 10; // max ~20
	while (1) {
		if (read_imu(&imu) != HAL_OK) MX_I2C1_Init();
		HAL_Delay(2);
		if (HAL_GetTick() - time_previous < t1) {
			setTarget(&motor1, target1);
			setTarget(&motor2, target1);
			setTarget(&motor3, target1);
		}
	}
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  /* USER CODE END 3 */
}

